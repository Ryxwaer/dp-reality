#!/usr/bin/env node
// Reads seed module bundles from server/assets/seed-modules/*.mjs,
// dynamically imports each to extract its `manifest` export, and emits
// server/seeds/generated-bundles.ts with both the bundle source and
// manifest so the seeder can insert module rows without any knowledge
// of what a specific module does.
import { readFile, writeFile, mkdir } from 'node:fs/promises'
import { resolve, dirname } from 'node:path'
import { fileURLToPath, pathToFileURL } from 'node:url'

const HERE = dirname(fileURLToPath(import.meta.url))
const ROOT = resolve(HERE, '..')
const SEED_DIR = resolve(ROOT, 'server/assets/seed-modules')
const OUT_FILE = resolve(ROOT, 'server/seeds/generated-bundles.ts')

const BUNDLES = [
  { exportName: 'SREALITY', fileName: 'sreality.mjs' },
  { exportName: 'BAZOS', fileName: 'bazos.mjs' }
]

function escapeTemplate(src) {
  return src
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$\{/g, '\\${')
}

async function loadEntry(bundle) {
  const path = resolve(SEED_DIR, bundle.fileName)
  const src = await readFile(path, 'utf8')
  const mod = await import(pathToFileURL(path).href)
  if (!mod.manifest || typeof mod.manifest !== 'object') {
    throw new Error(`${bundle.fileName}: missing \`export const manifest\``)
  }
  return { ...bundle, src, manifest: mod.manifest }
}

async function main() {
  const entries = await Promise.all(BUNDLES.map(loadEntry))
  await mkdir(dirname(OUT_FILE), { recursive: true })

  const header = [
    '// AUTO-GENERATED by scripts/sync-seed-bundles.mjs. DO NOT EDIT BY HAND.',
    '// Regenerate with `pnpm seed:sync`.',
    '// Source: server/assets/seed-modules/*.mjs',
    '',
    'import type { ModuleManifest } from \'../../../module-sdk/template/host-types\'',
    '',
    'export interface SeedBundle {',
    '  code: string',
    '  manifest: ModuleManifest',
    '}',
    ''
  ].join('\n')

  const body = entries.map((e) => {
    const escaped = escapeTemplate(e.src)
    const manifestJson = JSON.stringify(e.manifest, null, 2)
    return [
      `export const ${e.exportName}_BUNDLE: SeedBundle = {`,
      `  code: \`${escaped}\`,`,
      `  manifest: ${manifestJson} as ModuleManifest`,
      '}',
      ''
    ].join('\n')
  }).join('\n')

  await writeFile(OUT_FILE, header + body, 'utf8')
  console.log(`[sync-seed-bundles] wrote ${OUT_FILE} (${entries.length} bundles)`)
}

main().catch((err) => {
  console.error('[sync-seed-bundles] failed:', err)
  process.exit(1)
})
