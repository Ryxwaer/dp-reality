#!/usr/bin/env node
/**
 * Read the seed module bundles from `server/assets/seed-modules/` and emit a
 * TypeScript file (`server/seeds/generated-bundles.ts`) that exports their
 * contents as string constants. Embedding the bundles avoids any runtime
 * filesystem / storage registration fragility: the seeder just imports the
 * strings and upserts them.
 *
 * Run after editing or re-building one of the example modules:
 *
 *     pnpm seed:sync
 */
import { readFile, writeFile, mkdir } from 'node:fs/promises'
import { resolve, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const HERE = dirname(fileURLToPath(import.meta.url))
const ROOT = resolve(HERE, '..')
const SEED_DIR = resolve(ROOT, 'server/assets/seed-modules')
const OUT_FILE = resolve(ROOT, 'server/seeds/generated-bundles.ts')

const BUNDLES = [
  { exportName: 'SREALITY_BUNDLE', fileName: 'sreality.mjs' },
  { exportName: 'BAZOS_BUNDLE', fileName: 'bazos.mjs' }
]

async function main() {
  const entries = await Promise.all(BUNDLES.map(async (b) => {
    const src = await readFile(resolve(SEED_DIR, b.fileName), 'utf8')
    return { ...b, src }
  }))

  await mkdir(dirname(OUT_FILE), { recursive: true })

  const header = [
    '// AUTO-GENERATED by scripts/sync-seed-bundles.mjs. DO NOT EDIT BY HAND.',
    '// Regenerate with `pnpm seed:sync`.',
    '// Source: server/assets/seed-modules/*.mjs',
    ''
  ].join('\n')

  const body = entries.map((e) => {
    const escaped = e.src
      .replace(/\\/g, '\\\\')
      .replace(/`/g, '\\`')
      .replace(/\$\{/g, '\\${')
    return `export const ${e.exportName} = \`${escaped}\`\n`
  }).join('\n')

  await writeFile(OUT_FILE, header + body, 'utf8')
  console.log(`[sync-seed-bundles] wrote ${OUT_FILE} (${entries.length} bundles)`)
}

main().catch((err) => {
  console.error('[sync-seed-bundles] failed:', err)
  process.exit(1)
})
